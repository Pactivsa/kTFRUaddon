package cn.kuzuanpa.ktfruaddon.api.tile.computerCluster;

import net.minecraft.world.World;

public class UserContainerDummy implements IComputerClusterUserContainer{
    int x,y,z;
    World world;
    public UserContainerDummy(int x, int y,int z, World world){
        this.x = x;
        this.y = y;
        this.z = z;
        this.world = world;
    }
    @Override
    public int getX() {
        return x;
    }

    @Override
    public int getY() {
        return y;
    }

    @Override
    public int getZ() {
        return z;
    }

    @Override
    public World getWorld() {
        return world;
    }
}
